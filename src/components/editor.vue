        'event':'getManuscript',
        'data':{
          articleId:res.d.id}
        },"*");
        // 发送准备完毕事件
        this.changeBox();
      },error=>{
        this.errorInfo='错误:'+error.m;
        this.editorShow=false;
      })
    },
    // 获取文章信息
    getArticle(article_id,type){
      this.$socket("article@/article/info_article",{id:article_id,type:type},(res)=>{
        this.content = Base64.decode(res.d.content);
        this.articleId=res.d.id;
        this.attachmentId=res.d.attachment;
        // 发送准备完毕事件
        this.changeBox();
      },error=>{
        this.errorInfo='错误:'+error.m;
        this.editorShow=false;
      })
    },
    //监听方法
    message(d){
      let data = d.data;
      switch (data.event) {   
        // 检测到传值事件           
        case 'byValues':
        //存储sid
        this.$ls.set('ws-token',data.data.sid,config.token_time);
        if(data.data.article_id){
          this.getArticle(data.data.article_id,data.data.type);
        }else{
          this.getManuscript(data.data.type,data.data.sn);
        }
        break;
        // 检测到保存事件
        case 'save':
        if(this.articleId){
          this.save();
        }
        break;
      }
    }
  },
  components: {
    imglist
  },
  mounted(){
    // 发送准备完毕事件
    this.ready()
    window.addEventListener('message',this.message)   
  },
  beforeDestroy(){
    window.removeEventListener('message',this.message);
  }
};
