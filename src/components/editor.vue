            // 如果不上传图片到服务器，此处不必配置
            container: "#toolbar", // container为工具栏，此次引入了全部工具栏，也可自行配置
            // container:container,
            handlers: {
              image: function() {
                // 劫持原来的图片点击按钮事件
                QuillWatch.emit(this.quill.id);
              }
            }
          }
        }
      }
    };
  },
  methods: {
    //保存
    save(){
      let status=false;
      this.$socket("article@/article/save_article",{id:this.articleId,content:Base64.encode(this.content)},(res)=>{       
        if(res.d){
          this.$Message.success("保存成功!");
          status=true;
        }else{
          this.$Message.error("保存失败!");
        }
        window.parent.postMessage({
            'event':'save',
            'data':{
              status:status}
        },"*");
      },error=>{
        this.$Message.error(error.m);
        window.parent.postMessage({
            'event':'save',
            'data':{
              status:status}
        },"*");
      })
    },
    imgsClick() {
      this.imglistShow = true;
    },
    imgsInsert(arr) {
      this.imglistShow=false;
      // res为图片服务器返回的数据
      // 获取富文本组件实例
      let quill = this.$refs.myQuillEditor.quill;
      quill.focus();
      let length=0;
      for(let i of arr){
        // 获取光标所在位置
        length = quill.getSelection().index;
        quill.insertEmbed(length, "image", i);
        // 调整光标到最后
        quill.setSelection(length + 1);
      }
    },
    // 编辑器准备完毕
